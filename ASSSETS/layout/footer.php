Os signos do zodíaco têm origem em lendas de diversas culturas, como a babilônica, egípcia, grega e romana. A astrologia teve início na Mesopotâmia no segundo milênio a.C., quando as estrelas e os planetas eram usados para interpretar presságios dos deuses. 
O primeiro signo do zodíaco foi o Touro, há 4 mil anos, quando coincidia com a primavera e marcava o início do ano. A imagem do touro vem de um mito grego em que Zeus se transformou no animal para atrair uma princesa. 
Os signos do zodíaco são:
Áries, Touro, Gêmeos, Câncer, Leão, Virgem, Libra, Escorpião, Sagitário, Capricórnio, Aquário, Peixes.


